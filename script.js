//Show and hide medical team review box//

function MtYes() {
  var x = document.getElementById("Mt");
  if (x.style.display = "none") {
    x.style.display = "block";
  } else {
    x.style.display = "none";
  }
}

function MtNo() {
  var x = document.getElementById("Mt");
  if (x.style.display = "block") {
    x.style.display = "none";
  } else {
    x.style.display = "block";
  }
}


