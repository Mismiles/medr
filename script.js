//Show and hide allergybox//

function show() {
    var x = document.getElementById("AllergyBox");
    if (x.style.display = "none") {
      x.style.display = "block";
    } else {
      x.style.display = "none";
    }
  }

  function hide() {
    var x = document.getElementById("AllergyBox");
    if (x.style.display = "block") {
      x.style.display = "none";
    } else {
      x.style.display = "block";
    }
  }

